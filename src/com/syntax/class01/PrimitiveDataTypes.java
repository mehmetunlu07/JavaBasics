package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		// 8 primitive data types
		byte variableOne=10;  //to store whole numbers
		short variable2=100;
		int variable_3=1000;
		long variable$4=100000;
		
		float variable5=12.99f;   // to store decimal numbers
		double variable6=75.87;
		
		//to store single character
		char variable7='a';
		char variable8='8';
		char variable9='%';
		
		boolean hungry=true;  // to store boolean
		boolean thirsty=false;
		float variable10=12.456f;
		double variable11=12.456656565898945457878790909678889999999999995555;
		
		System.out.println(variable10);
	}

}
